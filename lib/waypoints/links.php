Global function:

Some examples of built-in global functions in JavaScript include: parseInt() : Converts a string to an integer. parseFloat() : Converts a string to a floating-point number. isNaN() : Checks if a value is Not-a-Number (NaN).

radix: 

A radix parameter specifies the number system to use: 2 = binary, 8 = octal, 10 = decimal, 16 = hexadecimal. If radix is omitted, JavaScript assumes radix 10. If the value begins with "0x", JavaScript assumes radix 16.


Aumber system:
A system for representing the counting numbers. These include: The usual "base ten" or "decimal" system: 1, 2, 3, … , 10, 11, 12, … Binary numbers are represented on the number system with '1s' and '0'
 
